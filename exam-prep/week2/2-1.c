#include <stdio.h> // #include <stdio.h> is a preprocessor directive that tells the compiler to include the contents of stdio.h in the program.

int main() // void main() may not return a value (int main() returns value))
{
    int n = 2004;
    printf("%d\n", n); // %d is a placeholder for an integer.
}

